import unittest

from day_8.day import Day


class day(unittest.TestCase):
    def test_result(self):
        day = Day()
        day._inputfilename = "test_input.txt"
        day.read_input()
        self.assertEqual(12, len(day.input))
        self.assertEqual(['.','.','.','.','.','.','.','.','.','.','.','.'], day.input[0])
        day.antinodes[0][0] = '#'
        self.assertEqual(1, day.nr_of_antinodes())
        day.antinodes[0][0] = '.'
        self.assertEqual(0, day.nr_of_antinodes())
        self.assertEqual([(8, 1),(5, 2),(7,3),(4,4)], day.find_antenna_pairs_xy('0', 1))
        day.mark_antinodes([(8, 1),(5, 2),(7,3),(4,4)])
        self.assertEqual(antinode_0, day.antinodes)
        day.map_antinodes()
        self.assertEqual(14, day.nr_of_antinodes())
        day_p2 = Day()
        day_p2._inputfilename = "test_input.txt"
        day_p2.read_input()
        day_p2.map_antinodes(all=True)
        self.assertEqual(antinodes_part2, day_p2.antinodes)
        self.assertEqual(34, day_p2.nr_of_antinodes())




